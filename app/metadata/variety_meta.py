INE_SYMBOLS = {"SC", "NR", "LU", "BC", "EC"}
GFEX_SYMBOLS = {"SI", "LC", "PS", "PT", "PD"}


def get_variety_name(symbol: str) -> str:
    meta = VARIETY_META_BY_SYMBOL.get((symbol or "").upper())
    return meta[0] if meta else (symbol or "")


def get_exchange_code(symbol: str, fallback: str = "") -> str:
    sym = (symbol or "").upper()
    if sym in INE_SYMBOLS:
        return "INE"
    if sym in GFEX_SYMBOLS:
        return "GFEX"
    meta = VARIETY_META_BY_SYMBOL.get(sym)
    if not meta:
        return fallback
    return EXCHANGE_NAME_TO_CODE.get(meta[2], fallback or meta[2])
